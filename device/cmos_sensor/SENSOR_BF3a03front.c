static void BF3A03F_hvblank(u32 h,u32 v)
{
	u32 i;
	const u8 t[][2] = {
		{0x2a,(h >> 8) << 4 },
		{0x2b,h & 0xff},
		{0x92,v & 0xff},
		{0x93,v >> 8},
		{0xe3,0x09},
		{0xe4,0},
	};
	
	for(i=0;i<6;i++)
		sensor_iic_write((u8 *)&t[i][0]);
	//	sensor_iic_disable();
}

